T = int(input())
for tc in range(T):
    n = int(input())

    dp = [0] * (n+1)

    # dp[1] = 1 (1)
    # dp[2] = 1+1 / 2 (2)
    # dp[3] = 1+1+1 / 2+1 / 1+2 / 3 (4)
    # dp[4] = 1+1+1+1 / 2+1+1 / 1+2+1 / 1+1+2 / 2+2 / 3+1 / 1+3 (7)
    # dp[5] = 1+1+1+1+1 / 2+1+1+1 / 1+2+1+1 / 1+1+2+1 / 1+1+1+2 / 2+2+1 / 2+1+2 / 1+2+2 / 3+1+1 / 1+3+1 / 1+1+3 / 3+2 / 2+3

    dp[1] = dp[0] + 1
    if n > 1:
        dp[2] = dp[1] + 1
    if n > 2:
        dp[3] = dp[2] + dp[1] + 1

    if n > 3:
        for i in range(4, n+1):
            dp[i] = dp[i-1] + dp[i-2] + dp[i-3]

    print(dp[n])